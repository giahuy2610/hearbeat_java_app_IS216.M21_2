--table post

CREATE OR REPLACE TRIGGER trigger_autoincre_tb_district_districtid BEFORE
    INSERT ON tb_district
    REFERENCING
        NEW AS new
    FOR EACH ROW
BEGIN
    SELECT
        auto_increment_sequence_district.NEXTVAL
    INTO :new.districtid
    FROM
        dual;

END;


