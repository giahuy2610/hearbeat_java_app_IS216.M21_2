    private void btn_xac_nhanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_xac_nhanMouseClicked
        if (jTextField1.getText().equals("")) {
            if (jTextField1.getText().equals("")) {
                jLabel11.setVisible(true);
            } else {
                jLabel11.setVisible(false);
            }
        } else {
            Connection conn = null;
            try {
                conn = nhapEmail.getConnection();
            } catch (SQLException ex) {
                Logger.getLogger(dangKy.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(dangKy.class.getName()).log(Level.SEVERE, null, ex);
            }
            String query = "";
            synchronized (query) {
                query = "select * from tb_user where email = '" + jTextField1.getText() + "'";
            }
            try ( Statement stmt = conn.createStatement()) {
                ResultSet rs = stmt.executeQuery(query);
                if(!rs.next()) {
                    JOptionPane.showMessageDialog(null, "Không tồn tại tài khoản khớp với email. Vui lòng nhập lại!", "", JOptionPane.INFORMATION_MESSAGE);
                    jTextField1.setText("");                    
                }
                else {
                    while (rs.next()) {
                        JOptionPane.showMessageDialog(null, "Mã khôi phục đã được gửi đến " + jTextField1.getText(), "", JOptionPane.INFORMATION_MESSAGE);
                        System.out.println(rs.getString("userid"));
                    }                
                    this.dispose();
                    new quenMatKhau(jTextField1.getText()).setVisible(true);
                }


            } catch (SQLException e) {
                System.out.println(e.getMessage());
            }

        }
    }//GEN-LAST:event_btn_xac_nhanMouseClicked

